package _3_TypeCasting;

//byte > short > char > int > long > float > double
public class _2_WideningTypeCasting {
	public static void main(String[] args) {
		int num1 = 5004;
		double num2 = 2.5;
		double sum = num1 + num2;
		System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);
	}
}